// Show a friendly label in the UI
export function labelFor(code: string): string {
    const all = (FULL_MADLAD_LANGUAGES?.length ? FULL_MADLAD_LANGUAGES : MADLAD_LANGUAGES);
    const item = all.find(l => l.code === code);
    if (!item) return code;
    return item.native ? `${item.name} ‚Ä¢ ${item.native}` : item.name;
}

// Runtime loader: if you later add /public/data/madlad_languages.json, this will use it.
// If not, it returns the in-file FULL_MADLAD_LANGUAGES (or the seed list if you didn‚Äôt paste).
let _fullCache: Lang[] | null = null;

export async function loadFullMadladLanguages(): Promise<Lang[]> {
    if (FULL_MADLAD_LANGUAGES && FULL_MADLAD_LANGUAGES.length > 0) {
        return FULL_MADLAD_LANGUAGES;
    }
    if (_fullCache) return _fullCache;
    try {
        const res = await fetch("/data/madlad_languages.json", { cache: "force-cache" });
        if (res.ok) {
            const arr = (await res.json()) as Lang[];
            const dedup = [{ code: AUTO_CODE, name: "Auto-detect" }, ...arr.filter(x => x.code !== AUTO_CODE)];
            _fullCache = dedup;
            return dedup;
        }
    } catch {}
    return MADLAD_LANGUAGES;
}
